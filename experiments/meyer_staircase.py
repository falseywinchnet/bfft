#!/usr/bin/env python3
"""Staircase statistics: TGFD cartoon vs converged Gilles cartoon.

Tests the inference that the converged alternation staircases the cartoon
(piecewise-constant plateaus separated by artificial jumps) while the
budgeted alternation retains smooth shading.

Measurements, per image, over all 20 test images:

A. Gradient-magnitude population.  A smooth ramp has small but NONZERO
   gradient everywhere; a staircase has |grad| ~ 0 on plateaus and large
   at the jumps.  So staircasing shows as mass leaving the "ramp" band
   and moving to BOTH the dead-flat band and the jump band.

B. The same statistics restricted to regions where the ORIGINAL image is
   locally smooth (low local variance of f).  This is the discriminant:
   where the scene has no structure, any plateau/jump pattern in the
   cartoon is generated by the method.

C. JPEG probe, reproducing the observed evidence objectively.  Both
   cartoons are encoded at identical quality; we measure the compression
   error, and the compression error restricted to the smooth mask (where
   ringing on flat ground is visible).  The codec is an instrument with
   no stake in the outcome.

Read-only with respect to all other files.
"""
from __future__ import annotations

import io
import sys
from pathlib import Path

import numpy as np

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))

from meyer_bregman import a2bc_budget, a2bc_nested_warm, grad  # noqa: E402
from meyer_supplement import NAMES, load, IMGS, LAM, MU  # noqa: E402


def gmag(u):
    gx, gy = grad(u)
    return np.sqrt(gx * gx + gy * gy)


def local_var(a, k=4):
    """Box local variance via cumulative sums, periodic-safe enough."""
    def box(x):
        c = np.cumsum(np.pad(x, ((k, k), (0, 0)), mode="reflect"), axis=0)
        x = (c[2 * k:, :] - c[:-2 * k, :]) / (2 * k)
        c = np.cumsum(np.pad(x, ((0, 0), (k, k)), mode="reflect"), axis=1)
        return (c[:, 2 * k:] - c[:, :-2 * k]) / (2 * k)
    m = box(a)
    return np.maximum(box(a * a) - m * m, 0.0)


# gradient-magnitude bands, gray levels per pixel
FLAT, RAMP_HI, JUMP = 0.05, 1.0, 5.0


def bands(g, mask=None):
    v = g[mask] if mask is not None else g.ravel()
    n = v.size
    return (np.count_nonzero(v < FLAT) / n,
            np.count_nonzero((v >= FLAT) & (v < RAMP_HI)) / n,
            np.count_nonzero(v >= JUMP) / n,
            float(np.median(v)))


def jpeg_probe(u, quality=75, mask=None):
    from PIL import Image
    a = np.clip(u, 0, 255)
    arr = a.astype(np.uint8)
    buf = io.BytesIO()
    Image.fromarray(arr).save(buf, "JPEG", quality=quality)
    size = buf.tell()
    buf.seek(0)
    dec = np.asarray(Image.open(buf)).astype(np.float64)
    err = dec - arr.astype(np.float64)
    rms = float(np.sqrt(np.mean(err ** 2)))
    rms_mask = (float(np.sqrt(np.mean(err[mask] ** 2)))
                if mask is not None and mask.any() else float("nan"))
    return size, rms, rms_mask


def main():
    print("Staircase statistics.  Bands of |grad u| in gray levels/pixel:")
    print(f"  flat  < {FLAT}   ramp [{FLAT}, {RAMP_HI})   jump >= {JUMP}")
    print("  smooth mask = lowest tercile of local variance of the ORIGINAL\n")

    hdr = (f"{'image':>10s} | {'flat%':>11s} {'ramp%':>11s} {'jump%':>11s} "
           f"{'median|g|':>13s} | {'smooth-region ramp%':>21s} "
           f"{'JPEG rms (mask)':>17s}")
    print(hdr)
    print(f"{'':>10s} | {'G / T':>11s} {'G / T':>11s} {'G / T':>11s} "
          f"{'G / T':>13s} | {'G / T':>21s} {'G / T':>17s}")
    print("-" * len(hdr))

    acc = {k: [] for k in ("flat_g", "flat_t", "ramp_g", "ramp_t",
                           "jump_g", "jump_t", "med_g", "med_t",
                           "sramp_g", "sramp_t", "sflat_g", "sflat_t",
                           "jr_g", "jr_t", "jsz_g", "jsz_t",
                           "jrm_g", "jrm_t")}

    for stem, title in NAMES.items():
        f = load(IMGS / f"{stem}.png")
        ug, vg, _ = a2bc_nested_warm(f, LAM, MU)
        ut, vt, _ = a2bc_budget(f, LAM, MU, budget_s=0.9 * f.size / 512 ** 2)

        lv = local_var(f)
        smooth = lv <= np.quantile(lv, 1.0 / 3.0)

        gg, gt = gmag(ug), gmag(ut)
        fg, rg, jg, mg = bands(gg)
        ft, rt, jt, mt = bands(gt)
        sfg, srg, _, _ = bands(gg, smooth)
        sft, srt, _, _ = bands(gt, smooth)
        szg, rmsg, rmsmg = jpeg_probe(ug, mask=smooth)
        szt, rmst, rmsmt = jpeg_probe(ut, mask=smooth)

        for k, v in (("flat_g", fg), ("flat_t", ft), ("ramp_g", rg),
                     ("ramp_t", rt), ("jump_g", jg), ("jump_t", jt),
                     ("med_g", mg), ("med_t", mt), ("sramp_g", srg),
                     ("sramp_t", srt), ("sflat_g", sfg), ("sflat_t", sft),
                     ("jr_g", rmsg), ("jr_t", rmst), ("jsz_g", szg),
                     ("jsz_t", szt), ("jrm_g", rmsmg), ("jrm_t", rmsmt)):
            acc[k].append(v)

        print(f"{title:>10s} | {100*fg:5.1f}/{100*ft:5.1f} "
              f"{100*rg:5.1f}/{100*rt:5.1f} {100*jg:5.2f}/{100*jt:5.2f} "
              f"{mg:6.3f}/{mt:6.3f} | {100*srg:9.1f}/{100*srt:10.1f} "
              f"{rmsmg:7.2f}/{rmsmt:8.2f}")

    m = {k: float(np.mean(v)) for k, v in acc.items()}
    print("-" * len(hdr))
    print(f"{'MEAN':>10s} | {100*m['flat_g']:5.1f}/{100*m['flat_t']:5.1f} "
          f"{100*m['ramp_g']:5.1f}/{100*m['ramp_t']:5.1f} "
          f"{100*m['jump_g']:5.2f}/{100*m['jump_t']:5.2f} "
          f"{m['med_g']:6.3f}/{m['med_t']:6.3f} | "
          f"{100*m['sramp_g']:9.1f}/{100*m['sramp_t']:10.1f} "
          f"{m['jrm_g']:7.2f}/{m['jrm_t']:8.2f}")

    print("\nSummary (G = Gilles converged, T = TGFD budgeted; means over 20):")
    print(f"  dead-flat pixel fraction     G {100*m['flat_g']:5.1f}%   "
          f"T {100*m['flat_t']:5.1f}%   ratio {m['flat_g']/m['flat_t']:.2f}")
    print(f"  ramp pixel fraction          G {100*m['ramp_g']:5.1f}%   "
          f"T {100*m['ramp_t']:5.1f}%   ratio {m['ramp_t']/m['ramp_g']:.2f}"
          f"  (T/G)")
    print(f"  jump pixel fraction          G {100*m['jump_g']:5.2f}%   "
          f"T {100*m['jump_t']:5.2f}%   ratio {m['jump_g']/m['jump_t']:.2f}")
    print(f"  median |grad u|              G {m['med_g']:.4f}   "
          f"T {m['med_t']:.4f}   ratio {m['med_t']/m['med_g']:.2f}  (T/G)")
    print(f"  SMOOTH REGIONS, flat frac    G {100*m['sflat_g']:5.1f}%   "
          f"T {100*m['sflat_t']:5.1f}%")
    print(f"  SMOOTH REGIONS, ramp frac    G {100*m['sramp_g']:5.1f}%   "
          f"T {100*m['sramp_t']:5.1f}%   ratio "
          f"{m['sramp_t']/m['sramp_g']:.2f}  (T/G)")
    print(f"  JPEG q75 rms error (all)     G {m['jr_g']:.3f}   "
          f"T {m['jr_t']:.3f}")
    print(f"  JPEG q75 rms error (smooth)  G {m['jrm_g']:.3f}   "
          f"T {m['jrm_t']:.3f}   ratio {m['jrm_g']/m['jrm_t']:.2f}  (G/T)")
    print(f"  JPEG q75 file size (bytes)   G {m['jsz_g']:.0f}   "
          f"T {m['jsz_t']:.0f}")


if __name__ == "__main__":
    main()
